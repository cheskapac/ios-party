import UIKit

extension UIColor {
    static let primary = #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1) // #333333
    static let secondary = #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1) // #FFFFFF
    static let tertiary = #colorLiteral(red: 0.501960814, green: 0.501960814, blue: 0.501960814, alpha: 1) // #808080
    
    static let header = UIColor.primary
    static let body = UIColor.primary
    static let caption = UIColor.tertiary
    static let input = UIColor.primary
    static let inputSeparator = UIColor.tertiary
}
